    lines.append("%  Copyright (c) 2026, REPLACE: Your Name")
    lines.append("%  Editors: A.D. Rollett & M. De Graef")
    lines.append("%  All rights reserved.")
    lines.append("%")
    lines.append("% Licensed under the Creative Commons CC BY-NC-SA 4.0 License,")
    lines.append('% hereafter referred to as the "License"; you may not use this')
