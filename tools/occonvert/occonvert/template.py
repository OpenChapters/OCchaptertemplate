"""LaTeX template generator for OpenChapters format.

Takes a Chapter IR object and emits:
- MyChapter.tex (the chapter source file)
- chapter.json (metadata for the OC web interface)
- chaptercitations.bib (bibliography)
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import TYPE_CHECKING

from .utils import derive_chabbr, make_label, runs_to_latex, slugify

if TYPE_CHECKING:
    from .model import (
        BibEntry,
        Block,
        Chapter,
        CodeBlock,
        Equation,
        Figure,
        ListBlock,
        ListItem,
        Paragraph,
        Section,
        Table,
    )

# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def generate_chapter_tex(chapter: Chapter, chabbr: str | None = None) -> str:
    """Generate the full MyChapter.tex content."""
    if not chabbr:
        chabbr = derive_chabbr(chapter.title or "MyChapter")

    title = chapter.title or "REPLACE: Your Chapter Title"
    chapter_label_name = slugify(title) if title != "REPLACE: Your Chapter Title" else "MyChapter"

    lines: list[str] = []
    used_labels: set[tuple[str, str]] = set()
    _emit_copyright(lines)
    _emit_author_setup(lines, chapter, chabbr)
    _emit_chapter_title(lines, title, chapter_label_name)
    _emit_writeauthors(lines, chapter, title, chapter_label_name)
    _emit_learning_objectives(lines, chapter)
    lines.append("")
    lines.append("% ###################################################################")
    lines.append("% CHAPTER CONTENT STARTS HERE")
    lines.append("% ###################################################################")
    lines.append("")
    for section in chapter.sections:
        _emit_section(lines, section, used_labels=used_labels)
    lines.append("% ###################################################################")
    lines.append("% END OF CHAPTER")
    lines.append("% ###################################################################")
    _emit_todo_summary(lines, chapter)
    return "\n".join(lines) + "\n"


def generate_chapter_json(
    chapter: Chapter,
    chabbr: str | None = None,
    metadata: dict | None = None,
) -> str:
    """Generate the chapter.json metadata file.

    ``metadata`` keys override the defaults derived from ``chapter``. Only keys
    that appear in the default schema are accepted; others are ignored.
    """
    if not chabbr:
        chabbr = derive_chabbr(chapter.title or "MyChapter")

    toc = [s.title for s in chapter.sections if s.level == 1 and s.title]

    data = {
        "title": chapter.title or "REPLACE: Your Chapter Title",
        "authors": [
            f"{a.first} {a.last}".strip() or "REPLACE: Your Name"
            for a in chapter.authors
        ]
        or ["REPLACE: Your Name"],
        "description": "REPLACE: A brief description of this chapter.",
        "toc": toc or ["Introduction"],
        "entry_file": "MyChapter.tex",
        "cover_image": "cover.png",
        "keywords": [],
        "chapter_type": chapter.chapter_type,
        "chabbr": chabbr,
        "depends_on": [],
        "discipline": "mse",
        "published": False,
    }
    if metadata:
        for key, value in metadata.items():
            if key in data and value is not None:
                data[key] = value
    return json.dumps(data, indent=2, ensure_ascii=False) + "\n"


def generate_bib(entries: list[BibEntry]) -> str:
    """Generate a BibTeX .bib file from BibEntry objects."""
    if not entries:
        return _default_bib()
    parts = [
        "%% This BibTeX bibliography file was generated by OCconvert.",
        "%% Please verify all entries and add DOIs where missing.",
        "",
    ]
    for e in entries:
        if "doi" not in e.fields:
            parts.append(f"%% TODO: add DOI for {e.key}")
        parts.append(f"@{e.entry_type}{{{e.key},")
        for k, v in e.fields.items():
            parts.append(f"\t{k} = {{{v}}},")
        parts.append("}")
        parts.append("")
    return "\n".join(parts) + "\n"


