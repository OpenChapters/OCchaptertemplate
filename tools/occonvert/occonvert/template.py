def _default_bib() -> str:
    return (
        "%% This BibTeX bibliography file was generated by OCconvert.\n"
        "%% Add your references here in BibTeX format.\n"
        "%% Citation keys should use the format: lastnameYEARa\n"
        "%% Example: smith2026a\n"
        "%% All entries should include a DOI field.\n\n"
    )


def _unique_slug(
    used: set[tuple[str, str]] | None, label_type: str, base: str
) -> str:
    """Return a label slug unique within ``label_type``, appending letter suffixes.

    The OC convention forbids numerals in labels, so collisions are resolved with
    ``b``, ``c``, ``d`` … (first occurrence keeps the bare slug). If ``used`` is
    None, the base slug is returned unchanged — callers without a registry get
    the old behaviour.
    """
    base = base or "unnamed"
    if used is None:
        return base
    key = (label_type, base)
    if key not in used:
        used.add(key)
        return base
    for c in "bcdefghijklmnopqrstuvwxyz":
        cand = f"{base}{c}"
        k = (label_type, cand)
        if k not in used:
            used.add(k)
            return cand
    # More than 25 collisions on one base — fall back to two-letter suffixes.
    for c1 in "abcdefghijklmnopqrstuvwxyz":
        for c2 in "abcdefghijklmnopqrstuvwxyz":
            cand = f"{base}{c1}{c2}"
            k = (label_type, cand)
            if k not in used:
                used.add(k)
                return cand
    return base


